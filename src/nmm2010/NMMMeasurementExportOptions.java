    private void buttonBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBrowseActionPerformed
        JFileChooser fc = new JFileChooser(this.nmmsetup.getProperty("NMM_SETUP_PROJECT_PATH", null));
        int returnVal = fc.showOpenDialog(this);


        if (returnVal == JFileChooser.APPROVE_OPTION) {
            file = fc.getSelectedFile();
            this.textfDestinationFile.setText(file.getName());           
        }
    }//GEN-LAST:event_buttonBrowseActionPerformed

    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed

        int aktywnyPomiar=this.nmmproj.getCurrentMeasurementNumber();
        int resampling=Integer.parseInt(this.comboResampleSelection.getSelectedItem().toString());
        System.out.println("Eksport aktywnego pomiaru: "+aktywnyPomiar+" z resamplingiem (rekord√≥w):"+resampling);
        int dlugoscPomiaru=this.nmmproj.getMeasurement(aktywnyPomiar).getMeasurementLength();
        int iloscNowychRekordow=dlugoscPomiaru/resampling;

        NMMMeasurement ms = this.nmmproj.getMeasurement(aktywnyPomiar);
        int counter=0;
        Double suma=0.0;
        DecimalFormat df = new DecimalFormat("###.0");
        try {
            FileWriter fr = new FileWriter(file);
            BufferedWriter br = new BufferedWriter(fr);

            for (int i=0; i<iloscNowychRekordow;i++) {
                suma=0.0;
                for (int oldSample=0; oldSample<resampling;oldSample++) {                
                    suma=suma+Math.pow(10, 0.1*ms.getRecordValue(counter, RecordValueType.LAeq));
                    counter++;
                }
                suma=10*Math.log10(suma/resampling);
                br.write(df.format(suma)+"\n");
            }           
            br.flush();
            br.close();
         } catch(IOException ex) {
         }
        this.dispose();
    }//GEN-LAST:event_buttonOKActionPerformed

   
