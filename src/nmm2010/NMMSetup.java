    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        String fileName=this.nmmSetup.getProperty("NMM_SETUP_LOGO", "ecoplan.png");
        File file = new File(fileName);
        this.textLogoFile.setText(file.getAbsolutePath());
        ImageIcon imgIcon = new ImageIcon(fileName);
        Image ni = imgIcon.getImage().getScaledInstance(200, 150, 1);
        ImageIcon smallImgIcon = new ImageIcon(ni);
        this.labelCompanyLogo.setIcon(smallImgIcon);
        this.labelCompanyLogo.setText("");
        this.comboLanguage.setSelectedItem(this.nmmSetup.getProperty("NMM_SETUP_LANGUAGE", "English"));
        
        String value="true";
        boolean result = value.equalsIgnoreCase(this.nmmSetup.getProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LMin", "true"));
        this.checkboxDisplayLMin.setSelected(result);
                
        result = value.equalsIgnoreCase(this.nmmSetup.getProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LMax", "true"));
        this.checkboxDisplayLMax.setSelected(result);
        
        result = value.equalsIgnoreCase(this.nmmSetup.getProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LEnd", "true"));
        this.checkboxDisplayLEnd.setSelected(result);
        
        result = value.equalsIgnoreCase(this.nmmSetup.getProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LStart", "true"));
        this.checkboxDisplayLStart.setSelected(result);
                
        this.textSELCriteria.setText(this.nmmSetup.getProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_SELCriteria", "10"));
        this.textPresentationsPath.setText(this.nmmSetup.getProperty("NMM_SETUP_PRESENTATIONSDIR", "c:\\"));
        
        result = value.equalsIgnoreCase(this.nmmSetup.getProperty("NMM_SETUP_LOAD_PREVIOUS_PROJECT_AT_STARTUP", "true"));
        this.boxLoadPreviousProject.setSelected(result);
        
        
    }//GEN-LAST:event_formWindowOpened

    private void buttonBrowseLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBrowseLogoActionPerformed

        JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            this.textLogoFile.setText(file.getAbsolutePath());
            this.nmmSetup.setProperty("NMM_SETUP_LOGO", file.getAbsolutePath());
        }
        String fileName = this.nmmSetup.getProperty("NMM_SETUP_LOGO", "ecoplan.png");
        ImageIcon imgIcon = new ImageIcon(fileName);
        Image ni = imgIcon.getImage().getScaledInstance(200, 150, 1);
        ImageIcon smallImgIcon = new ImageIcon(ni);
        this.labelCompanyLogo.setIcon(smallImgIcon);
        this.labelCompanyLogo.setText("");
    }//GEN-LAST:event_buttonBrowseLogoActionPerformed

