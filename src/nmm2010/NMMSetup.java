    private void jbuttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonSaveActionPerformed
        this.nmmSetup.setProperty("NMM_SETUP_PROJECT_PATH", this.jtextProjectPath.getText());
        this.nmmSetup.setProperty("NMM_SETUP_COMPANY", this.textCompany.getText());
        this.nmmSetup.setProperty("NMM_SETUP_USER", this.textUser.getText());
        this.nmmSetup.setProperty("NMM_SETUP_LOGO", this.textLogoFile.getText());
        this.nmmSetup.setProperty("NMM_SETUP_INTERFACE_NUMBERFORMAT", this.textNumberFormat.getText());        
        this.nmmSetup.setProperty("NMM_SETUP_LANGUAGE", this.comboLanguage.getSelectedItem().toString());        
        this.nmmSetup.setProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LStart", String.valueOf(this.checkboxDisplayLStart.isSelected())); 
        this.nmmSetup.setProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LEnd", String.valueOf(this.checkboxDisplayLEnd.isSelected()));
        this.nmmSetup.setProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LMax", String.valueOf(this.checkboxDisplayLMax.isSelected()));
        this.nmmSetup.setProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_LMin", String.valueOf(this.checkboxDisplayLMin.isSelected()));
        this.nmmSetup.setProperty("NMM_SETUP_FREE_SELECTION_DISPLAY_SELCriteria", this.textSELCriteria.getText());
        this.nmmSetup.setProperty("NMM_SETUP_CLEAR_SELECTION_AFTER_EVENT", String.valueOf(this.checkboxClearSelectionAfterEvent.isSelected()));
        this.nmmSetup.setProperty("NMM_SETUP_LOAD_PREVIOUS_PROJECT_AT_STARTUP", String.valueOf(this.boxLoadPreviousProject.isSelected()));
        this.nmmSetup.setProperty("NMM_SETUP_PRESENTATIONSDIR", this.textPresentationsPath.getText());
        this.nmmSetup.setProperty("NMM_SETUP_PREVIOUS_PROJECT", this.currentProject.toString() );
        this.nmmSetup.saveNMMConfigurationToFile();
        JOptionPane.showMessageDialog(null,"Restart NMM in order to reload settings!","NMM 2010 * Information",JOptionPane.INFORMATION_MESSAGE);        
        this.dispose();
    }//GEN-LAST:event_jbuttonSaveActionPerformed

