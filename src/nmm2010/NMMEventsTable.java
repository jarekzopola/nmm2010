    private void recalculateStatistics() {
        //System.out.println("Wybieram:"+this.comboIndicator.getSelectedIndex()+1);
        tm.setIndicatorType(this.comboIndicator.getSelectedIndex()+1);
        tm.setEventType(new NMMEventType(this.comboEventTypes.getSelectedItem().toString()));
        tm.fireTableDataChanged();
        //super.paintComponents(super.getGraphics());
    }

