    private void tableEventsSummaryMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEventsSummaryMouseReleased
         int selCol=this.tableEventsSummary.getSelectedColumn();
        if (selCol>2) {
            tm.setEventType(new NMMEventType(this.comboEventTypes.getSelectedItem().toString()));
            //super.paintComponents(super.getGraphics());
            this.labelStandardDeviation.setText("Standard deviation = "+NMMToolbox.formatDouble(
                    NMMNoiseCalculator.calculateStandardDeviation(
                    tm.getColumn(selCol))));
            this.labelAverage.setText("Average = "+NMMToolbox.formatDouble(
                    NMMNoiseCalculator.SredniaLog(tm.getColumn(selCol))));
            this.labelUncertaintyA.setText("Uncertainty A = "+NMMToolbox.formatDouble(
                    NMMNoiseCalculator.Lord95(tm.getColumn(selCol))));
        } else {
            this.labelStandardDeviation.setText("Standard deviation = ");
            this.labelAverage.setText("Average = --,-");
            this.labelUncertaintyA.setText("Uncertainty A = --,-");
        }

    }//GEN-LAST:event_tableEventsSummaryMouseReleased

