    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        this.nmmEv.setStart(TimeConverter.StringToLong(this.textStartDate.getText(),
                this.textStartHour.getText(), this.st));
        this.nmmEv.setEnd(TimeConverter.StringToLong(this.textEndDate.getText(),
                this.textEndHour.getText(), this.st));
        this.nmmEv.setColor(this.buttonColor.getBackground());
        this.nmmEv.setDescription(this.textEventDescription.getText());
        this.nmmEv.setEnabled(this.checkboxEnabled.isSelected());
        this.nmmEv.setEventType((NMMEventType)this.comboboxEventType.getSelectedItem());
    }//GEN-LAST:event_buttonUpdateActionPerformed

    private void comboboxEventTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboboxEventTypeActionPerformed
        
        JComboBox _cb = (JComboBox)evt.getSource();
        if (_cb.getItemCount()>0) {
            NMMEventType cet = (NMMEventType)this.comboboxEventType.getSelectedItem();
            this.textEventDescription.setText(cet.getDescription());
            this.buttonColor.setBackground(cet.getColor());
        }
    }//GEN-LAST:event_comboboxEventTypeActionPerformed

