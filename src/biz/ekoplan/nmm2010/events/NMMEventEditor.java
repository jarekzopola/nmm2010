    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        System.out.println("Ustawiam kontrolki na oknie dialogu edycji zdarzenia-----------------------------");        
        this.textStartDate.setText(TimeConverter.LongToDateString(this.nmmEv.getStart(), DateFormat.MEDIUM,
               this.st));
        this.textStartHour.setText(TimeConverter.LongToTimeString(this.nmmEv.getStart(), DateFormat.MEDIUM,
               this.st));
        this.textEndDate.setText(TimeConverter.LongToDateString(this.nmmEv.getEnd(), DateFormat.MEDIUM,
               this.st));
        this.textEndHour.setText(TimeConverter.LongToTimeString(this.nmmEv.getEnd(), DateFormat.MEDIUM,
               this.st));
        this.checkboxEnabled.setSelected(this.nmmEv.isEnabled());        
        this.comboboxEventType.removeAllItems();
        int noet = this.nmmProj.getEventTypeTypes().length;
        NMMEventType[] ets = new NMMEventType[noet];
        ets= this.nmmProj.getEventTypeTypes();
        for (int i=0; i<noet;i++) {
            this.comboboxEventType.addItem(ets[i]);
            System.out.println("Dodaje do listy typ: "+ets[i]+" klasa: "+ets[i]);
        }
        this.comboboxEventType.setSelectedItem(this.nmmEv.getEventType());
        this.buttonColor.setBackground(this.nmmEv.getColor());
        this.textEventDescription.setText(this.nmmEv.getDescription());
        System.out.println("Ustawiam kolor przycisku na: "+this.nmmEv.getColor().toString());
        System.out.println("Ustawiam typ zdarzenia na: "+this.nmmEv.getEventType().toString());
        System.out.println("Zakończyłem ustawianie kontrolek okna dialogu edycji zdarzenia.------------------");
    }//GEN-LAST:event_formWindowOpened

