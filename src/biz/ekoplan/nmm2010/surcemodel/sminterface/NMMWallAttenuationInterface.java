    @Override
    public boolean isComplete() {
        
        boolean completeForm=false;        
        if (this.comboEventsAfterImision.getSelectedIndex()!=-1 &&
                this.comboEventsAfterReference.getSelectedIndex()!=-1 &&
                this.comboEventsBeforeImision.getSelectedIndex()!=-1 &&
                this.comboEventsBeforeReference.getSelectedIndex()!=-1 &&
                this.comboFieldType.getSelectedIndex()!=-1 &&
                this.comboMeasurementAfterImision.getSelectedIndex()!=-1 &&
                this.comboMeasurementAfterReference.getSelectedIndex()!=-1 &&
                this.comboMeasurementBeforeReference.getSelectedIndex()!=-1 &&
                this.comboMeasurementBeforeImision.getSelectedIndex()!=-1)
        {
            completeForm=true;
        }       
        return completeForm;
    }

