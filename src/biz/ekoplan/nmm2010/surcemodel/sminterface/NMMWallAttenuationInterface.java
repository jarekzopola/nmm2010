    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        
        //ustawiamy nazwę modelu
        this.nmmNoiseWallAttenuationModel.setModelName((this.textCalculationModelName.getText()));        
        //if calculation model isn't in the project yet, than add it
        if (this.isComplete() && !this.nmmNoiseWallAttenuationModel.getProject().contains(this.nmmNoiseWallAttenuationModel)) {                    
            System.out.println("Tego modelu źródła jeszcze nie ma, dodaję ...");
            this.nmmNoiseWallAttenuationModel.getProject().addNoiseSourceModel(this.nmmNoiseWallAttenuationModel);
        }
        this.dispose();
    }//GEN-LAST:event_buttonOKActionPerformed

